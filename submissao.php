  <h3 class="centered">DA INSCRIÇÃO</h3>
  <p class="centered">Poderão ser submetidos para apresentação apenas trabalhos na modalidade <strong>RESUMOS SIMPLES</strong> de acordo com os critérios gerais:</p>
  <ul class="centered">
    <li>O autor principal deve estar obrigatoriamente inscrito no evento.</li>
    <li>Para cada inscrição, serão aceitos até dois trabalhos por autor e ilimitadas coautorias.</li>
    <li>Será permitida a inscrição de trabalhos assinados por um autor e até, no máximo, 5 (cinco) coautores, incluindo o orientador.</li>
    <li>No ato de submissão, obrigatoriamente, deverá ser anexada uma carta de autorização do orientador.</li>
    <li>Os resumos dos trabalhos devem ser enviados para seleção exclusivamente por meio do site.</li>
    <li>Não haverá cobrança pela publicação dos resumos.</li>
  </ul>

  <h3 class="centered">DAS CATEGORIAS</h3>
  <p class="centered">Serão aceitos, independentemente da área temática, as seguintes categorias:</p>
  <ul class="centered">
    <li>Pesquisa observacional ou experimental: com abordagem quantitativa e/ou qualitativa.</li>
    <li>Revisão de literatura.</li>
    <li>Relato de caso.</li>
    <li>Relato de experiência.</li>
  </ul>

  <h3 class="centered">DOS EIXOS TEMÁTICOS</h3>
  <h4 class="centered">Eixo 1 – Saúde e Sociedade</h4>
  <p class="centered">Pesquisas voltadas para os estudos com aplicação de métodos qualitativos e quantitativos para avaliação de abordagens, processos, serviços, recursos e produtos tecnológicos que visam monitorar, preservar e melhorar a saúde e qualidade de vida dos seres humanos.</p>

  <h4 class="centered">Eixo 2 – Tecnologias em Saúde</h4>
  <p class="centered">Pesquisas que envolvem o desenvolvimento e o aprimoramento de produtos e processos tecnológicos aplicados à saúde; Criação e aplicação de testes, de novos modelos de avaliação clínica e experimental, tais como desenvolvimento e/ou validação de instrumentos para diagnóstico e tratamento, para procedimentos, processos, produtos e serviços.</p>

  <h3 class="centered">DA NORMATIZAÇÃO DOS TRABALHOS CIENTÍFICOS – RESUMO SIMPLES</h3>
  <ul class="centered">
    <li>O resumo deverá ser redigido em português do Brasil, de acordo com as normas gramaticais e ortográficas em vigor.</li>
    <li>O resumo simples deverá ser elaborado em Português com limite mínimo de 300 palavras e máximo de 400 palavras. O texto deve ser digitado em Word, fonte Times New Roman, título tamanho 14, corpo tamanho 12, margem superior de 3,0 cm, lateral esquerda de 3,0 cm, margem inferior de 2,0 cm e lateral direita 2,0 cm.</li>
    <li>O corpo do resumo deve conter, no máximo, 400 palavras, divididas em diferentes seções, dependendo do tipo de trabalho.</li>
  </ul>

  <h3 class="centered">DA SUBMISSÃO E AVALIAÇÃO</h3>
  <ul class="centered">
    <li>Os trabalhos deverão ser submetidos exclusivamente via site do evento.</li>
    <li>Cada pesquisador poderá submeter até 2 (dois) trabalhos como autor e ilimitadas participações como coautorias.</li>
    <li>Alterações de qualquer natureza, após a submissão do trabalho, só serão aceitas caso solicitadas pelos avaliadores.</li>
  </ul>

  <h3 class="centered">DA PREMIAÇÃO</h3>
  <p class="centered">Após avaliação dos trabalhos submetidos, os 03 (três) melhores trabalhos de cada eixo temático, julgados pela comissão científica/avaliadora, receberão menções honrosas a serem enviadas por e-mail em forma de certificado. A divulgação dos premiados ocorrerá no último dia do evento e a certificação será enviada por e-mail em até 15 dias úteis.</p>

  <h3 class="centered">DA APRESENTAÇÃO</h3>
  <p class="centered">Os autores dos trabalhos selecionados deverão preparar uma apresentação em slides e em vídeo, de acordo com o tipo de trabalho. O tempo de apresentação será de no máximo 10 minutos para apresentações orais e 3 minutos para apresentações de pôster/vídeo (slides com áudio).</p>

  <h3 class="centered">DAS DISPOSIÇÕES FINAIS</h3>
  <ul class="centered">
    <li>Os certificados serão emitidos aos autores no site do evento em até 30 dias úteis.</li>
    <li>Os trabalhos serão organizados em ANAIS no site do evento com ISBN e ficha catalográfica.</li>
  </ul>

